local basefunc = require "Game/Common/basefunc"

Act_045_XXLBDEXTRAPanel = basefunc.class()
local C = Act_045_XXLBDEXTRAPanel
C.name = "Act_045_XXLBDEXTRAPanel"
local M = Act_045_XXLBDManger

function C.Create()
	return C.New()
end

function C:AddMsgListener()
    for proto_name,func in pairs(self.lister) do
        Event.AddListener(proto_name, func)
    end
end

function C:MakeLister()
    self.lister = {}
    self.lister["ExitScene"]=basefunc.handler(self,self.MyExit)
end

function C:RemoveListener()
    for proto_name,func in pairs(self.lister) do
        Event.RemoveListener(proto_name, func)
    end
    self.lister = {}
end

function C:MyExit()
	self:ClearExtraItemBase()
	self:RemoveListener()
	destroy(self.gameObject)
end

function C:OnDestroy()
	self:MyExit()
end

function C:MyClose()
	self:MyExit()
end

function C:ctor()
	ExtPanel.ExtMsg(self)
	local parent = GameObject.Find("Canvas/LayerLv5").transform
	local obj = newObject(C.name, parent)
	local tran = obj.transform
	self.transform = tran
	self.gameObject = obj
	LuaHelper.GeneratingVar(self.transform, self)
	
	self:MakeLister()
	self:AddMsgListener()
	self:InitUI()
end

function C:InitUI()
	self.back_btn.onClick:AddListener(function ()
		self:MyExit()
	end)
	self:MyRefresh()
end

function C:MyRefresh()
	self:CreateExtraItemBase()
end

function C:CreateExtraItemBase()
	self:ClearExtraItemBase()
	local tab = M.GetCurExtraAwardConfig()
	for i=1,#tab do
		local pre = Act_045_XXLBDEXTRAItemBase.Create(self.Content.transform,i)
		self.extra_cell[#self.extra_cell + 1] = pre
	end
end

function C:ClearExtraItemBase()
	if self.extra_cell then
		for k,v in pairs(self.extra_cell) do
			v:MyExit()
		end
	end
	self.extra_cell = {}
end