local basefunc = require "Game/Common/basefunc"

Act_045_XXLBDEXTRAItemBase = basefunc.class()
local C = Act_045_XXLBDEXTRAItemBase
C.name = "Act_045_XXLBDEXTRAItemBase"
local M = Act_045_XXLBDManger

function C.Create(parent,index)
	return C.New(parent,index)
end

function C:AddMsgListener()
    for proto_name,func in pairs(self.lister) do
        Event.AddListener(proto_name, func)
    end
end

function C:MakeLister()
    self.lister = {}
    self.lister["ExitScene"]=basefunc.handler(self,self.MyExit)
end

function C:RemoveListener()
    for proto_name,func in pairs(self.lister) do
        Event.RemoveListener(proto_name, func)
    end
    self.lister = {}
end

function C:MyExit()
	self:RemoveListener()
	destroy(self.gameObject)
end

function C:OnDestroy()
	self:MyExit()
end

function C:MyClose()
	self:MyExit()
end

function C:ctor(parent,index)
	ExtPanel.ExtMsg(self)
	self.index = index
	local parent = parent or GameObject.Find("Canvas/GUIRoot").transform
	local obj = newObject(C.name, parent)
	local tran = obj.transform
	self.transform = tran
	self.gameObject = obj
	LuaHelper.GeneratingVar(self.transform, self)
	
	self:MakeLister()
	self:AddMsgListener()
	self:InitUI()
end

function C:InitUI()
	self:MyRefresh()
end

function C:MyRefresh()
	self.index_txt.text = self.index
	self.award_txt.text = M.GerCurExtraAward(self.index) .. "福利券"
	self.conditions_txt.text = "累计赢金" .. StringHelper.ToCash(M.GetCurExtraCondition(self.index)*10000)
end
