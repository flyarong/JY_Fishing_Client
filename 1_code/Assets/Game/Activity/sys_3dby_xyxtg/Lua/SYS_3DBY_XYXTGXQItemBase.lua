local basefunc = require "Game/Common/basefunc"

SYS_3DBY_XYXTGXQItemBase = basefunc.class()
local C = SYS_3DBY_XYXTGXQItemBase
C.name = "SYS_3DBY_XYXTGXQItemBase"
local M = SYS_3DBY_XYXTGManager

function C.Create(parent,data)
	return C.New(parent,data)
end

function C:AddMsgListener()
    for proto_name,func in pairs(self.lister) do
        Event.AddListener(proto_name, func)
    end
end

function C:MakeLister()
    self.lister = {}
    self.lister["ExitScene"]=basefunc.handler(self,self.MyExit)
end

function C:RemoveListener()
    for proto_name,func in pairs(self.lister) do
        Event.RemoveListener(proto_name, func)
    end
    self.lister = {}
end

function C:MyExit()
	self:RemoveListener()
	destroy(self.gameObject)
end

function C:OnDestroy()
	self:MyExit()
end

function C:MyClose()
	self:MyExit()
end

function C:ctor(parent,data)
	ExtPanel.ExtMsg(self)
	self.data = data
	local parent = parent or GameObject.Find("Canvas/GUIRoot").transform
	local obj = newObject(C.name, parent)
	local tran = obj.transform
	self.transform = tran
	self.gameObject = obj
	LuaHelper.GeneratingVar(self.transform, self)
	
	self:MakeLister()
	self:AddMsgListener()
	self:InitUI()
end

function C:InitUI()
	self:MyRefresh()
end

function C:MyRefresh()
	self.name_txt.text = M.GetGameNameDescByGameName(self.data.game_name)
	self.je_txt.text = self.data.bet_money
	self.bl_txt.text = self.data.tot_round - self.data.remain_round
	self.yj_txt.text = self.data.award_money
end