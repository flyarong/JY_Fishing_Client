local basefunc = require "Game/Common/basefunc"

CPL_LJYJCFKNotVipPanel = basefunc.class()
local C = CPL_LJYJCFKNotVipPanel
C.name = "CPL_LJYJCFKNotVipPanel"
local M = CPL_LJYJCFKManager
function C.Create()
	return C.New()
end

function C:AddMsgListener()
    for proto_name,func in pairs(self.lister) do
        Event.AddListener(proto_name, func)
    end
end

function C:MakeLister()
    self.lister = {}
    self.lister["ExitScene"]=basefunc.handler(self,self.MyExit)
end

function C:RemoveListener()
    for proto_name,func in pairs(self.lister) do
        Event.RemoveListener(proto_name, func)
    end
    self.lister = {}
end

function C:MyExit()
	self:RemoveListener()
	destroy(self.gameObject)
end

function C:OnDestroy()
	self:MyExit()
end

function C:MyClose()
	self:MyExit()
end

function C:ctor()
	local parent = GameObject.Find("Canvas/LayerLv4").transform
	local obj = newObject(C.name, parent)
	local tran = obj.transform
	self.transform = tran
	self.gameObject = obj
	LuaHelper.GeneratingVar(self.transform, self)
	
	self:MakeLister()
	self:AddMsgListener()
	self:InitUI()
end

function C:InitUI()
	EventTriggerListener.Get(self.back_btn.gameObject).onClick = basefunc.handler(self, self.OnBackClick)
	EventTriggerListener.Get(self.upvip_btn.gameObject).onClick = basefunc.handler(self, self.OnUpVipClick)
	self:MyRefresh()
end

function C:MyRefresh()
end

function C:OnBackClick()
	self:MyExit()
end

function C:OnUpVipClick()
	PayPanel.Create(GOODS_TYPE.jing_bi, "normal")
	self:MyExit()
end