local basefunc = require "Game/Common/basefunc"

SYS_Exit_AskItemBase = basefunc.class()
local C = SYS_Exit_AskItemBase
C.name = "SYS_Exit_AskItemBase"
local M = SYS_Exit_AskManager

function C.Create(parent,config,decs,call,callback)
	return C.New(parent,config,decs,call,callback)
end

function C:AddMsgListener()
    for proto_name,func in pairs(self.lister) do
        Event.AddListener(proto_name, func)
    end
end

function C:MakeLister()
    self.lister = {}
    self.lister["ExitScene"]=basefunc.handler(self,self.MyExit)
end

function C:RemoveListener()
    for proto_name,func in pairs(self.lister) do
        Event.RemoveListener(proto_name, func)
    end
    self.lister = {}
end

function C:MyExit()
	self:RemoveListener()
	destroy(self.gameObject)
end

function C:OnDestroy()
	self:MyExit()
end

function C:MyClose()
	self:MyExit()
end

function C:ctor(parent,config,decs,call,callback)
	ExtPanel.ExtMsg(self)
    self.config = config
    self.decs = decs
    self.call = call
    self.callback = callback
	local parent = parent or GameObject.Find("Canvas/GUIRoot").transform
	local obj = newObject(C.name, parent)
	local tran = obj.transform
	self.transform = tran
	self.gameObject = obj
	LuaHelper.GeneratingVar(self.transform, self)
	
	self:MakeLister()
	self:AddMsgListener()
	self:InitUI()
end

function C:InitUI()
    EventTriggerListener.Get(self.enter_btn.gameObject).onClick = basefunc.handler(self, self.OnEnterClick)
	self:MyRefresh()
end

function C:MyRefresh()
    self.icon_img.sprite = GetTexture(self.config.icon_img)
    self.name_txt.text = self.config.name_txt
    self.dec_txt.text = self.decs
end

function C:OnEnterClick()
    if self.call then
        self.call()
    end
    if self.callback then
        self.callback()
    end
end