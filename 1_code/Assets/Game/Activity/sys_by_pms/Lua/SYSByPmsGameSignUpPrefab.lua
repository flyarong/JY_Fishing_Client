local basefunc = require "Game/Common/basefunc"

SYSByPmsGameSignUpPrefab = basefunc.class()
local C = SYSByPmsGameSignUpPrefab
C.name = "SYSByPmsGameSignUpPrefab"
local M = SYSByPmsManager
function C.Create(parent)
	return C.New(parent)
end

function C:AddMsgListener()
    for proto_name,func in pairs(self.lister) do
        Event.AddListener(proto_name, func)
    end
end

function C:MakeLister()
    self.lister = {}
end

function C:RemoveListener()
    for proto_name,func in pairs(self.lister) do
        Event.RemoveListener(proto_name, func)
    end
    self.lister = {}
end

function C:MyExit()
	self:RemoveListener()
	destroy(self.gameObject)
end

function C:OnDestroy()
	self:MyExit()
end

function C:MyClose()
	self:MyExit()
end

function C:ctor(parent)
	local parent = parent or GameObject.Find("Canvas/GUIRoot").transform
	local obj = newObject(C.name, parent)
	local tran = obj.transform
	self.transform = tran
	self.gameObject = obj
	LuaHelper.GeneratingVar(self.transform, self)
	
	self:MakeLister()
	self:AddMsgListener()
	self:InitUI()
end

function C:InitUI()
	self.signup_btn.onClick:AddListener(function ()
	    ExtendSoundManager.PlaySound(audio_config.game.com_but_confirm.audio_name)
		self:OnSignClick()
	end)


	CommonHuxiAnim.Start(self.signup_btn.gameObject,1)
	local data = M.GetPMSGameIDToConfig(FishingModel.game_id - 1)
	self.title_txt.text = data.game_name
	self:MyRefresh()
end

function C:MyRefresh()
end

function C:OnSignClick()
	GameButtonManager.RunFun({gotoui = "sys_by_pms", data={id = FishingModel.game_id - 1}}, "signup")
end