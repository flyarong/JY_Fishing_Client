local basefunc = require "Game/Common/basefunc"

VipShowVIP4HKSPanel = basefunc.class()
local C = VipShowVIP4HKSPanel
C.name = "VipShowVIP4HKSPanel"
local M = VIPManager

function C.Create(parent)
	return C.New(parent)
end

function C:AddMsgListener()
    for proto_name,func in pairs(self.lister) do
        Event.AddListener(proto_name, func)
    end
end

function C:MakeLister()
    self.lister = {}
    self.lister["ExitScene"] = basefunc.handler(self,self.MyExit)
end

function C:RemoveListener()
    for proto_name,func in pairs(self.lister) do
        Event.RemoveListener(proto_name, func)
    end
    self.lister = {}
end

function C:MyExit()
	self:RemoveListener()
	destroy(self.gameObject)
end

function C:OnDestroy()
	self:MyExit()
end

function C:MyClose()
	self:MyExit()
end

function C:ctor(parent)
	ExtPanel.ExtMsg(self)
	local parent = parent or GameObject.Find("Canvas/GUIRoot").transform
	local obj = newObject(C.name, parent)
	local tran = obj.transform
	self.transform = tran
	self.gameObject = obj
	LuaHelper.GeneratingVar(self.transform, self)
	
	self:MakeLister()
	self:AddMsgListener()
	self:InitUI()
end

function C:InitUI()
    PlayerPrefs.SetInt(VIPManager.key .. MainModel.UserInfo.user_id .. "vip4hks", os.time())
    self.go_btn.onClick:AddListener(
        function ()
            ExtendSoundManager.PlaySound(audio_config.game.com_but_cancel.audio_name)
            GameManager.CommonGotoScence({gotoui = "game_FishingMatchHall", goto_scene_parm = {index = 2}}) 
        end
    )
	self:MyRefresh()
end

function C:MyRefresh()
end
