local basefunc = require "Game/Common/basefunc"

Act_TY_JZSJBEXTRAItemBase = basefunc.class()
local C = Act_TY_JZSJBEXTRAItemBase
C.name = "Act_TY_JZSJBEXTRAItemBase"
local M = Act_TY_JZSJBManager

function C.Create(parent,index,config)
	return C.New(parent,index,config)
end

function C:AddMsgListener()
    for proto_name,func in pairs(self.lister) do
        Event.AddListener(proto_name, func)
    end
end

function C:MakeLister()
    self.lister = {}
    self.lister["ExitScene"]=basefunc.handler(self,self.MyExit)
end

function C:RemoveListener()
    for proto_name,func in pairs(self.lister) do
        Event.RemoveListener(proto_name, func)
    end
    self.lister = {}
end

function C:MyExit()
	self:RemoveListener()
	destroy(self.gameObject)
end

function C:OnDestroy()
	self:MyExit()
end

function C:MyClose()
	self:MyExit()
end

function C:ctor(parent,index,config)
	ExtPanel.ExtMsg(self)
	self.index = index
	self.config = config
	local parent = parent or GameObject.Find("Canvas/GUIRoot").transform
	local obj = newObject(C.name, parent)
	local tran = obj.transform
	self.transform = tran
	self.gameObject = obj
	LuaHelper.GeneratingVar(self.transform, self)
	
	self:MakeLister()
	self:AddMsgListener()
	self:InitUI()
end

function C:InitUI()
	self:MyRefresh()
end

function C:MyRefresh()
	local path = M.GetCurSytleKey()
	SetTextureExtend(self.bg1_img, path.."_ewjl_bg_3")
	self.index_txt.text = self.index
    local str = ""
    for i=1,#self.config.award_txt do 
        if i > 1 then
            str = str .. " + " .. self.config.award_txt[i]
        else
            str = str .. self.config.award_txt[i]
        end
    end
	self.award_txt.text = str
	self.conditions_txt.text = self.config.condition
end
