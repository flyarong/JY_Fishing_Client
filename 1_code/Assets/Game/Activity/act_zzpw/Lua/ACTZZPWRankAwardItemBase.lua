local basefunc = require "Game/Common/basefunc"

ACTZZPWRankAwardItemBase = basefunc.class()
local C = ACTZZPWRankAwardItemBase
C.name = "ACTZZPWRankAwardItemBase"
local M = ACTZZPWManager

function C.Create(parent,config,index)
	return C.New(parent,config,index)
end

function C:AddMsgListener()
    for proto_name,func in pairs(self.lister) do
        Event.AddListener(proto_name, func)
    end
end

function C:MakeLister()
    self.lister = {}
    self.lister["ExitScene"]=basefunc.handler(self,self.MyExit)
end

function C:RemoveListener()
    for proto_name,func in pairs(self.lister) do
        Event.RemoveListener(proto_name, func)
    end
    self.lister = {}
end

function C:MyExit()
	self:RemoveListener()
	destroy(self.gameObject)
end

function C:OnDestroy()
	self:MyExit()
end

function C:MyClose()
	self:MyExit()
end

function C:ctor(parent,config,index)
	ExtPanel.ExtMsg(self)
    self.config = config
    self.index = index
	local parent = parent or GameObject.Find("Canvas/GUIRoot").transform
	local obj = newObject(C.name, parent)
	local tran = obj.transform
	self.transform = tran
	self.gameObject = obj
	LuaHelper.GeneratingVar(self.transform, self)
	
    self.outline = self.rank_txt.transform:GetComponent("Outline")

	self:MakeLister()
	self:AddMsgListener()
	self:InitUI()
end

function C:InitUI()
	self:MyRefresh()
end

function C:MyRefresh()
    local outline = {{94,202,106},{43,48,169},{202,155,80},{146,71,71},{97,20,136},{145,29,36}}
    local index1 = math.ceil(self.index / 3)
    self.rank_txt.text = self.config.rank_name
    self.outline.effectColor = Color.New(outline[index1][1]/255,outline[index1][2]/255,outline[index1][3]/255,1)
    self.award_txt.text = self.config.rank_award
end
