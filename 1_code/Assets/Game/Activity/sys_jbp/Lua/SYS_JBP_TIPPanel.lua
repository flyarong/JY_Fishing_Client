local basefunc = require "Game/Common/basefunc"

SYS_JBP_TIPPanel = basefunc.class()
local C = SYS_JBP_TIPPanel
C.name = "SYS_JBP_TIPPanel"

function C.Create()
	return C.New()
end

function C:AddMsgListener()
    for proto_name,func in pairs(self.lister) do
        Event.AddListener(proto_name, func)
    end
end

function C:MakeLister()
    self.lister = {}
    self.lister["ExitScene"]=basefunc.handler(self,self.MyExit)
end

function C:RemoveListener()
    for proto_name,func in pairs(self.lister) do
        Event.RemoveListener(proto_name, func)
    end
    self.lister = {}
end

function C:MyExit()
	self:StopAutoExitTimer()
	self:RemoveListener()
	destroy(self.gameObject)
end

function C:OnDestroy()
	self:MyExit()
end

function C:MyClose()
	self:MyExit()
end

function C:ctor()
	ExtPanel.ExtMsg(self)
	local parent = GameObject.Find("Canvas/LayerLv5").transform
	local obj = newObject(C.name, parent)
	local tran = obj.transform
	self.transform = tran
	self.gameObject = obj
	LuaHelper.GeneratingVar(self.transform, self)
	
	self:MakeLister()
	self:AddMsgListener()
	self:InitUI()
end

function C:InitUI()
	self:AutoExitTimer(true)
	self:MyRefresh()
end

function C:MyRefresh()
end

function C:AutoExitTimer(b)
	self:StopAutoExitTimer()
	if b then
		self.auto_exit_timer = Timer.New(function ()
			self:MyExit()
		end,3,1,false)
		self.auto_exit_timer:Start()
	end
end

function C:StopAutoExitTimer()
	if self.auto_exit_timer then
		self.auto_exit_timer:Stop()
		self.auto_exit_timer = nil
	end
end