local basefunc = require "Game/Common/basefunc"

SYSBYLevelPTItemBase = basefunc.class()
local C = SYSBYLevelPTItemBase
C.name = "SYSBYLevelPTItemBase"

function C.Create(parent,data,type,name)
	return C.New(parent,data,type,name)
end

function C:AddMsgListener()
    for proto_name,func in pairs(self.lister) do
        Event.AddListener(proto_name, func)
    end
end

function C:MakeLister()
    self.lister = {}
    self.lister["ExitScene"]=basefunc.handler(self,self.MyExit)
end

function C:RemoveListener()
    for proto_name,func in pairs(self.lister) do
        Event.RemoveListener(proto_name, func)
    end
    self.lister = {}
end

function C:MyExit()
	self:RemoveListener()
	destroy(self.gameObject)
end

function C:OnDestroy()
	self:MyExit()
end

function C:MyClose()
	self:MyExit()
end

function C:ctor(parent,data,type,name)
	self.url_or_img = data
	self.type = type
	self.name = name
	local parent = parent or GameObject.Find("Canvas/GUIRoot").transform
	local obj = newObject(C.name, parent)
	local tran = obj.transform
	self.transform = tran
	self.gameObject = obj
	LuaHelper.GeneratingVar(self.transform, self)
	
	self:MakeLister()
	self:AddMsgListener()
	self:InitUI()
end

function C:InitUI()
	if self.type == "url" then
		URLImageManager.UpdateHeadImage(self.url_or_img, self.head_img)
		self.name_txt.text = "新头像"
	elseif self.type == "img" then
		self.head_img.sprite = GetTexture(self.url_or_img)
		self.head_img:SetNativeSize()
		self.name_txt.text = self.name
	end

	self:MyRefresh()
end

function C:MyRefresh()
end
